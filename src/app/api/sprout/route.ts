function shuffle<T>(arr: T[]): T[] {
  const a = [...arr];
  for (let i = a.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [a[i], a[j]] = [a[j], a[i]];
  }
  return a;
}

export async function POST(req: NextRequest) {
  try {
    const { scores }: { scores: number[] } = await req.json();
    if (!Array.isArray(scores) || scores.length !== 6) {
      return NextResponse.json({ error: 'Invalid scores' }, { status: 400 });
    }

    const total = scores.reduce((a, b) => a + b, 0);
    const phase = phases.find(p => total >= p.range[0] && total <= p.range[1]) ?? phases[4];
    const phaseIdx = phases.indexOf(phase);
    const pool = actionPools[phaseIdx] ?? actionPools[4];
    const actions = shuffle(pool).slice(0, 3);
    const links = refLinks[phase.category] ?? refLinks.early;

    return NextResponse.json({
      phaseName: phase.name,
      phaseEmoji: phase.emoji,
      phaseReason: phase.reason,
      phaseAdvice: phase.advice,
      actions,
      links,
    });
  } catch {
    return NextResponse.json({ error: 'Calculation failed' }, { status: 500 });
  }
}
